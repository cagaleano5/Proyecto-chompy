    public JDConsultar_Clientes(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        DefaultTableModel dfm = new DefaultTableModel();
        ResultSet rs = objClienteCtl.mostrartodos_clienteCtl();
        if (rs == null) {
            JOptionPane.showMessageDialog(null, "No hay clientes registrados");
        } else {
            try {

                String v[] = {"NOMBRE", "IDENTIFICACIÓN", "DIRECCIÓN", "TELÉFONO", "CORREO"};
                dfm.setColumnIdentifiers(v);
                while (rs.next()) {
                    v[0] = rs.getString(1);
                    v[1] = rs.getString(2);
                    v[2] = rs.getString(3);
                    v[3] = rs.getString(4);
                    v[4] = rs.getString(5);
                    dfm.addRow(v);
                }
                this.jTable1.setModel(dfm);
            } catch (Exception e) {
//           JOptionPane.showMessageDialog(null, "Error en la Conexion");
            }
        }

    }

