    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         try {
            int id=Integer.parseInt(txtNumero_Identifica.getText());
             ResultSet rs = objClienteCtl.buscar_clienteCtl(id);
             if (rs!=null){
                rs.next();
                this.cbxTipo_Documento.setSelectedItem(rs.getString(1));
                this.txtNombre_Cliente.setText(rs.getString(2));
                this.txtDireccion.setText(rs.getString(3));
                this.txtTelefono.setText(rs.getString(4));
                this.txtCorreo.setText(rs.getString(5));
                this.txtNom_Contacto.setText(rs.getString(6));
                this.txtTel_Contacto.setText(rs.getString(7));
                this.txtNumero_Identifica.setEnabled(false);
             }else{
                JOptionPane.showMessageDialog(null, "Cliente no existe");
                this.txtNumero_Identifica.requestFocus();
                this.txtNumero_Identifica.setText("");
             }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Cliente no existe");
           this.txtNumero_Identifica.requestFocus();
           this.txtNumero_Identifica.setText("");
       }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String tipo, nom, ape, direc, tel, mail, nom_contact,tel_contact,rs;
        int id;
        id=Integer.parseInt(txtNumero_Identifica.getText());
        tipo=(String) cbxTipo_Documento.getSelectedItem().toString();
        nom=txtNombre_Cliente.getText().toUpperCase();
        direc=txtDireccion.getText().toUpperCase();
        tel=txtTelefono.getText();
        mail=txtCorreo.getText().toLowerCase();
        nom_contact=txtNom_Contacto.getText().toUpperCase();
        tel_contact=txtTel_Contacto.getText();
        rs=objClienteCtl.actualizar_clienteCtl(id, tipo, nom, direc,tel,mail,nom_contact,tel_contact);
        JOptionPane.showMessageDialog(this,rs);
        limpiarcliente();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void txtNumero_IdentificaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumero_IdentificaKeyReleased
         if(!txtNumero_Identifica.getText().matches("[0-9]*")){//.matches valida el rango de números a ingresar
            JOptionPane.showMessageDialog(null,"Solo números","Advertencia",JOptionPane.ERROR_MESSAGE);//mensaje con título en la caja y con ícono
            this.txtNumero_Identifica.setText("");//limpiar caja de texto
            this.txtNumero_Identifica.requestFocus();//lleva el cursor a la caja de texto
        }
    }//GEN-LAST:event_txtNumero_IdentificaKeyReleased

